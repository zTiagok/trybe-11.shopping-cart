const retrieveItens = (product) => {
  const productsUrl = `https://api.mercadolibre.com/sites/MLB/search?q=${product}`;

  fetch(productsUrl)
  .then((response) => response.json())
  .then((data) => {
    data.results.forEach((result) => {
      createProductItemElement(result);
    });
  })
  .catch((error) => error); 
};
